#include <iostream>
using namespace std;
// Comparison operators
int main()
{
   int a = 9, b = 2;

    cout<<"The value of a == b is "<<(a== b)<<endl;
    cout<<"The value of a !=b is "<<(a!= b)<<endl;
    cout<<"The value of a < b is "<<(a <b)<<endl;
    cout<<"The value of a > b is "<<(a >b)<<endl;
    cout<<"The value of a <= b is "<<(a <=b)<<endl;
    cout<<"The value of a>= b is "<<(a >=b)<<endl;
  

    return 0;
}