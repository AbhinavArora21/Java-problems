#include <iostream>
using namespace std;
int main()
{

int number = 10, result;
result = number;
cout<<"result = "<< result <<endl;
result += number;  
cout<<"result = "<< result <<endl;
result -= number;  
cout<<"result = "<< result <<endl;
result *= number;  
cout<<"result = "<< result <<endl;
result /= number;  
cout<<"result = "<< result <<endl;
result %= number;  
cout<<"result = "<< result <<endl;
return 0; 
}