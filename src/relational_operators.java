public class relational_operators {
    public static void main(String[] args) {
        int a,b,c;
        a=5;
        b=10;
        c=15;
        boolean g=(a>b), f=(b>c), h=(a==b), n=(a!=c);
        System.out.println(g);
        System.out.println(h);
        System.out.println(n);
        System.out.println(f);
    }
}
